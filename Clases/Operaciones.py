#introduciendo dos numeros
numeroA = 50
numeroB = 60
#Sumando dos numeros
sumar = numeroA + numeroB
print("La suma de los numero A y B es", sumar)
#Restando dos numeros
restar = numeroA - numeroB
print("La resta de los numero A y B es", restar)
#Multiplicando dos numeros
multiplicar= numeroA * numeroB
print("La multiplicacion de los numero A y B es", multiplicar)
#Dividiendo dos numeros
dividir= numeroA / numeroB
print(f"La division dio {dividir} exitosamente")
#Potenciando dos numeros
exponente = numeroA**numeroB
print(f"La exponente dio {exponente} exitosamente")
